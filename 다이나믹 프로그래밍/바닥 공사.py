# 바닥 공사 : 가로 길이가 N, 세로 길이가 2인 직사각형의 바닥을 채우는 모든 경우의 수를 구하는 프로그램
# 1X2, 2X1, 2X2의 덮개를 이용해 채운다.

# 입력 조건
# 첫째 줄에 N이 주어진다. (1<=N<=1000)

# 출력 조건
# 첫째 줄에 2XN 크기의 바닥을 채우는 방법의 수를 796,796으로 나눈 나머지를 출력한다. -> 결괏값이 매우 커질 수 있으므로

# 보텀업 방식

N = int(input())

d = [0]*1001

d[1] = 1
d[2] = 3
for i in range(3, N+1):
    d[i] = (d[i-1] + 2 * d[i-2]) % 796796

print(d[N])

